function البادئة_المشتركة_الأطول(النصوص) {
    if (النصوص.length === 0) {
        return ""; // إذا كانت المصفوفة فارغة، فليس هناك بادئة مشتركة.
    }

    // قم بفرز المصفوفة للتأكد من أن النص الأقصر في البداية.
    النصوص.sort((a, b) => a.length - b.length);

    const الأقصر = النصوص[0];
    let البادئة_المشتركة = "";

    for (let i = 0; i < الأقصر.length; i++) {
        const حرف = الأقصر[i];
        
        // تحقق مما إذا كان الحرف مشتركًا في جميع النصوص.
        if (النصوص.every(نص => نص[i] === حرف)) {
            البادئة_المشتركة += حرف;
        } else {
            break; // توقف إذا لم يكن الحرف مشتركًا.
        }
    }

    return البادئة_المشتركة;
}

// مثال على الاستخدام:
const النصوص = ["بناء", "بناؤون", "بنيان"];
const النتيجة = البادئة_المشتركة_الأطول(النصوص);
console.log("البادئة المشتركة الأطول:", النتيجة); // النتيجة: "بن"